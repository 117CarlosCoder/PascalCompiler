    public void panelReporte( LinkedList<Errores> errores){

        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setRowCount(0);  // Limpiar los datos existentes
        int i = 1;
        for (Errores error : errores) {
            Object[] row = new Object[5];
            row[0] = i++;
            row[1] = error.getTipo();
            row[2] = error.getDesc();
            row[3] = error.getLinea();
            row[4] = error.getColumna();
            modelo.addRow(row);
        }
    }


